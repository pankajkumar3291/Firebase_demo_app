package com.pank.pankapp.model;

public class FirebaseChat {

    private String firebaseId;
    private String firebaseMessage;

    public FirebaseChat() {

    }

    public String getFirebaseId() {
        return firebaseId;
    }

    public void setFirebaseId(String firebaseId) {
        this.firebaseId = firebaseId;
    }

    public String getFirebaseMessage() {
        return firebaseMessage;
    }

    public void setFirebaseMessage(String firebaseMessage) {
        this.firebaseMessage = firebaseMessage;
    }

}
